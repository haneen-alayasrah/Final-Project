         <h2 class="about-title"><span><strong>What is Accessible Jordan?&nbsp;</strong></span></h2>
         <p>Whether you have a mobility impairment, are a senior citizen, or have a baby you push around in a stroller, sometimes you want to go to a place without having to worry about whether or not they have stairs, ramps or elevators for easy access. It is very difficult to find places that are equipped and accessible around Jordan but when you do it's like winning a prize!</p>
